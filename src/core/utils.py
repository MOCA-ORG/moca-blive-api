# -- Utilities --------------------------------------------------------------------------

def print_info(msg: str) -> None:
    print('\033[32m' + '[INFO] ' + msg + '\033[0m')


def print_warning(msg: str) -> None:
    print('\033[33m' + '[WARNING] ' + msg + '\033[0m')


def print_error(msg: str) -> None:
    print('\033[31m' + '[ERROR] ' + msg + '\033[0m')

